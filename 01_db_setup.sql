USE SCHEMA CORPORATE_BANKING_DB.STRUCTURED_DATA;

-- 顧客一覧（業種別）
SELECT 
    INDUSTRY_CATEGORY,
    COUNT(*) AS CUSTOMER_COUNT,
    SUM(ANNUAL_REVENUE) AS TOTAL_REVENUE_MM
FROM CUSTOMER
GROUP BY INDUSTRY_CATEGORY
ORDER BY TOTAL_REVENUE_MM DESC;

-- 融資残高ランキング
SELECT 
    c.CUSTOMER_NAME,
    c.INDUSTRY_CATEGORY,
    l.OUTSTANDING_BALANCE AS LOAN_BALANCE_MM,
    l.LOAN_PURPOSE,
    l.RELATED_SUBSIDY
FROM LOAN l
JOIN CUSTOMER c ON l.CUSTOMER_ID = c.CUSTOMER_ID
WHERE l.RECORD_DATE = '2025-01-31'
ORDER BY l.OUTSTANDING_BALANCE DESC
LIMIT 10;

-- 預金残高（企業別合計）
SELECT 
    c.CUSTOMER_NAME,
    SUM(d.BALANCE) AS TOTAL_DEPOSIT_MM
FROM DEPOSIT d
JOIN CUSTOMER c ON d.CUSTOMER_ID = c.CUSTOMER_ID
WHERE d.RECORD_DATE = '2025-01-31'
GROUP BY c.CUSTOMER_NAME
ORDER BY TOTAL_DEPOSIT_MM DESC;

-- 預貸率分析
SELECT 
    c.CUSTOMER_NAME,
    SUM(l.OUTSTANDING_BALANCE) AS LOAN_BALANCE,
    SUM(d.BALANCE) AS DEPOSIT_BALANCE,
    ROUND(SUM(l.OUTSTANDING_BALANCE) / NULLIF(SUM(d.BALANCE), 0) * 100, 1) AS LOAN_DEPOSIT_RATIO
FROM CUSTOMER c
LEFT JOIN LOAN l ON c.CUSTOMER_ID = l.CUSTOMER_ID AND l.RECORD_DATE = '2025-01-31'
LEFT JOIN DEPOSIT d ON c.CUSTOMER_ID = d.CUSTOMER_ID AND d.RECORD_DATE = '2025-01-31'
GROUP BY c.CUSTOMER_NAME
HAVING SUM(d.BALANCE) > 0
ORDER BY LOAN_DEPOSIT_RATIO DESC;

-- 営業活動状況（案件ステージ別）
SELECT 
    DEAL_STAGE,
    COUNT(*) AS ACTIVITY_COUNT,
    SUM(DEAL_AMOUNT) AS TOTAL_DEAL_AMOUNT_MM
FROM SALES_ACTIVITY
WHERE DEAL_STAGE IS NOT NULL
GROUP BY DEAL_STAGE
ORDER BY TOTAL_DEAL_AMOUNT_MM DESC;


