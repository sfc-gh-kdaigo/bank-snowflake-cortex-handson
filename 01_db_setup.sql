-- ---------------------------------------------------------
-- Step 2-1: 法人顧客マスタ
-- ---------------------------------------------------------
CREATE OR REPLACE TABLE CUSTOMER (
    CUSTOMER_ID NUMBER(10,0) NOT NULL COMMENT '顧客ID',
    CUSTOMER_NAME VARCHAR NOT NULL COMMENT '企業名',
    CUSTOMER_NAME_KANA VARCHAR COMMENT '企業名カナ',
    INDUSTRY_CATEGORY VARCHAR NOT NULL COMMENT '業種カテゴリ（半導体製造装置、半導体材料、半導体デバイス、電子部品、半導体商社）',
    INDUSTRY_DETAIL VARCHAR COMMENT '業種詳細',
    MAIN_PRODUCT VARCHAR COMMENT '主要製品・サービス',
    ESTABLISHMENT_DATE DATE COMMENT '設立日',
    CAPITAL NUMBER(15,0) COMMENT '資本金（百万円）',
    EMPLOYEE_COUNT NUMBER(10,0) COMMENT '従業員数',
    ANNUAL_REVENUE NUMBER(15,0) COMMENT '年間売上高（百万円）',
    FISCAL_YEAR_END NUMBER(2,0) COMMENT '決算月',
    HEAD_OFFICE_ADDRESS VARCHAR COMMENT '本社所在地',
    HEAD_OFFICE_PREFECTURE VARCHAR COMMENT '本社都道府県',
    LISTED_MARKET VARCHAR COMMENT '上場市場',
    STOCK_CODE VARCHAR COMMENT '証券コード',
    CREDIT_RATING VARCHAR COMMENT '信用格付',
    RELATIONSHIP_START_DATE DATE COMMENT '取引開始日',
    RELATIONSHIP_MANAGER VARCHAR COMMENT '担当者名',
    BRANCH_NAME VARCHAR COMMENT '担当支店',
    GLOBAL_RANK VARCHAR COMMENT '世界での位置づけ',
    NOTES TEXT COMMENT '備考',
    CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT pk_customer PRIMARY KEY (CUSTOMER_ID)
) COMMENT = '法人顧客マスタ（半導体関連企業30社）';

-- ---------------------------------------------------------
-- Step 2-2: 融資情報
-- ---------------------------------------------------------
CREATE OR REPLACE TABLE LOAN (
    LOAN_ID NUMBER(10,0) NOT NULL COMMENT '融資ID',
    CUSTOMER_ID NUMBER(10,0) NOT NULL COMMENT '顧客ID',
    LOAN_TYPE VARCHAR NOT NULL COMMENT '融資種別（設備資金、運転資金、M&A資金等）',
    LOAN_PURPOSE VARCHAR COMMENT '資金使途詳細',
    LOAN_AMOUNT NUMBER(15,0) NOT NULL COMMENT '融資金額（百万円）',
    OUTSTANDING_BALANCE NUMBER(15,0) NOT NULL COMMENT '融資残高（百万円）',
    INTEREST_RATE NUMBER(5,3) COMMENT '適用金利（%）',
    LOAN_START_DATE DATE NOT NULL COMMENT '融資実行日',
    LOAN_END_DATE DATE COMMENT '返済期限',
    REPAYMENT_METHOD VARCHAR COMMENT '返済方法',
    COLLATERAL_TYPE VARCHAR COMMENT '担保種類',
    COLLATERAL_VALUE NUMBER(15,0) COMMENT '担保評価額（百万円）',
    GUARANTEE_TYPE VARCHAR COMMENT '保証種類',
    LOAN_STATUS VARCHAR NOT NULL COMMENT 'ステータス（実行中、完済、延滞等）',
    RELATED_SUBSIDY VARCHAR COMMENT '関連する補助金・助成金',
    RECORD_DATE DATE NOT NULL COMMENT '記録基準日',
    CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT pk_loan PRIMARY KEY (LOAN_ID, RECORD_DATE)
) COMMENT = '融資情報';

-- ---------------------------------------------------------
-- Step 2-3: 預金残高
-- ---------------------------------------------------------
CREATE OR REPLACE TABLE DEPOSIT (
    CUSTOMER_ID NUMBER(10,0) NOT NULL COMMENT '顧客ID',
    ACCOUNT_TYPE VARCHAR NOT NULL COMMENT '口座種別（普通預金、当座預金、定期預金等）',
    BALANCE NUMBER(15,0) NOT NULL COMMENT '残高（百万円）',
    PREVIOUS_MONTH_BALANCE NUMBER(15,0) COMMENT '前月残高（百万円）',
    AVERAGE_BALANCE NUMBER(15,0) COMMENT '平残（百万円）',
    RECORD_DATE DATE NOT NULL COMMENT '記録基準日（月末）',
    CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT pk_deposit PRIMARY KEY (CUSTOMER_ID, ACCOUNT_TYPE, RECORD_DATE)
) COMMENT = '預金残高（月次）';

-- ---------------------------------------------------------
-- Step 2-4: 営業活動履歴
-- ---------------------------------------------------------
CREATE OR REPLACE TABLE SALES_ACTIVITY (
    ACTIVITY_ID NUMBER(10,0) NOT NULL COMMENT '活動ID',
    CUSTOMER_ID NUMBER(10,0) NOT NULL COMMENT '顧客ID',
    ACTIVITY_DATE DATE NOT NULL COMMENT '活動日',
    ACTIVITY_TYPE VARCHAR NOT NULL COMMENT '活動種別（訪問、電話、メール、オンライン面談、提案等）',
    ACTIVITY_PURPOSE VARCHAR COMMENT '活動目的',
    CONTACT_PERSON VARCHAR COMMENT '面談相手',
    CONTACT_TITLE VARCHAR COMMENT '面談相手役職',
    ACTIVITY_SUMMARY TEXT COMMENT '活動概要',
    NEXT_ACTION VARCHAR COMMENT 'ネクストアクション',
    DEAL_STAGE VARCHAR COMMENT '案件ステージ（情報収集、提案中、交渉中、成約、失注）',
    DEAL_AMOUNT NUMBER(15,0) COMMENT '想定案件金額（百万円）',
    SALES_REP VARCHAR COMMENT '担当者',
    CREATED_AT TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    CONSTRAINT pk_sales_activity PRIMARY KEY (ACTIVITY_ID)
) COMMENT = '営業活動履歴';


